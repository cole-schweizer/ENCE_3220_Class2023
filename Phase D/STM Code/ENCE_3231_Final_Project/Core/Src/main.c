int speedometer(int converted, int wheelSize){
	int rpm;
	int circ;
	int speed;
	rpm = (int)converted*MAXRPM/100;
	circ = 3.14159*wheelSize;
	speed = circ*rpm;
	return speed;

}
int tachometer(int converted){
	int rpm;
	rpm = (int)converted*MAXRPM/100;
	return rpm;
}

